/**
 * Obtém uma cor do sistema de design
 */
export const getColor = (colorPath: string): string => {
  const keys = colorPath.split('.');
  let value: any = colors;
  
  for (const key of keys) {
    value = value[key];
    if (value === undefined) {
      console.warn(`Cor não encontrada: ${colorPath}`);
      return colors.gray[500];
    }
  }
  
  return value;
};

/**
 * Obtém um valor de espaçamento
 */
export const getSpacing = (size: keyof typeof spacing): string => {
  return spacing[size];
};

/**
 * Obtém um valor de tipografia
 */
export const getTypography = (type: keyof typeof typography.fontSize): string => {
  return typography.fontSize[type];
};

/**
 * Obtém um valor de sombra
 */
export const getShadow = (size: keyof typeof boxShadow): string => {
  return boxShadow[size];
};

