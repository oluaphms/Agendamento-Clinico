// Utilitários para cores
export const getColor = (color: string, shade: number = 500) => {
  const [colorName, colorShade] = color.split('-');
  const actualShade = colorShade ? parseInt(colorShade) : shade;
  return designTokens.colors[colorName as keyof typeof designTokens.colors]?.[actualShade as keyof typeof designTokens.colors.primary] || color;
};

